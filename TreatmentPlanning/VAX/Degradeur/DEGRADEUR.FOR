C-------------------------------------------------------------------------------
C       PROGRAMME D'INTERROGATION DU FICHIER DE LA BIBLIOTHEQUE 
C	DES DEGRADEURS ET DES COMBINAISONS A 2 POSSIBLES AVANT
C       LA CREATION D'UN NOUVEAU DEGRADEUR.
C                    
C                          DEGRADEUR.FOR	J.HERAULT  10-01-92
C-------------------------------------------------------------------------------

	DIMENSION PARCOURS(100),SOLU(20)
	REAL*4 PARCOURS,XEPS,DEGRADEUR
	INTEGER*2 N,MK
	CHARACTER*5 SOLU*11,SOMM1,SOMM2
	N=0
	OPEN (UNIT=20,FILE='[JOEL.ALPHA.BRAGG.DABRAG]DEGRA_92.DAT',STATUS='OLD')
	READ (20,*)
	DO I=1,100
		READ (20,5,END=77) PARCOURS(I)
		MK=I
	END DO
  77	CONTINUE
  5	FORMAT (F5.1)
	WRITE (*,*) 'ENTRER L''EPAISSEUR DU REDUCTEUR:'
	READ (*,*) DEGRADEUR
	DO I=1,MK
		XEPS=ABS(PARCOURS(I)-DEGRADEUR)
		IF (XEPS.LT.0.11) THEN
			N=N+1
			WRITE (SOLU(N),5) PARCOURS(I)
		END IF
	END DO
	DO I=1,MK-1
		DO J=I+1,MK
	   		SOMM=PARCOURS(I)+PARCOURS(J)
			XEPS=ABS(SOMM-DEGRADEUR)
	     		IF (XEPS.LT.0.11) THEN
				N=N+1
		       		WRITE (SOMM1,5) PARCOURS(I)     	
		   		WRITE (SOMM2,5) PARCOURS(J)
				SOLU(N)=SOMM1//'+'//SOMM2     
	      		END IF
		END DO
	END DO
	IF (N.EQ.0) THEN
		WRITE (*,*) 'AUCUN REDUCTEUR DISPONIBLE A 0.1 PRES'
	ELSE
		WRITE (*,*) 'SOLUTIONS PROPOSEES A 0.1 PRES:'
		DO I=1,N
			WRITE (*,*) SOLU(I)
		END DO
	END IF
	END
     	
